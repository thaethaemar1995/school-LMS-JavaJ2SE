    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        

        String coursename = txtName.getText();
        int fee = Integer.parseInt(txtFee.getText());
        String duration=txtDuration.getText();

        stu.setName(coursename);
        stu.setFee(fee);
        stu.setDuration(duration);

        try {
            db.RegisterCourse(stu);
            JOptionPane.showMessageDialog(this, "Register successful");
        } catch (SQLException el) {
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(StudentRegistrationForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddActionPerformed

