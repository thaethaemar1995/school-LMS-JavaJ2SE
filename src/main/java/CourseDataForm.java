    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        try {
            // TODO add your handling code here:
            Student stu=new Student();

            int fee=Integer.parseInt(txtFee1.getText());
            String duration=txtDuration1.getText();

            String coursename=String.valueOf(cboCourseName.getSelectedItem());

            stu.setFee(fee);
            stu.setDuration(duration);

            stu.setCourseName(coursename);

            db.UpdateCourse(stu);
            JOptionPane.showMessageDialog(this,"Update successful");
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(UpdateForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnEditActionPerformed

